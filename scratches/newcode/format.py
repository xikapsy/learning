#字符串格式化

#10.8f ，10宽度统一为10，小数点8位,f代表浮点数，d代表整数
print( format(10.1,"10.8f"),10.123334,10.3453)
print( format(10.12,"10.8f"),10.12323334,10.231)
print( format(10.123,"10.8f"),10.1243334,10.3532)
print( format(10.1234,"10.8f"),10.1236334,12054)
print( format(10.12345,"10.8f"),10.1233734,10.234)
print( format(10.123456,"10.8f"),10.12334,10.987)

#10.8f ，10宽度统一为10，小数点8位,>右对齐，<左对齐
print( format(1012.1,">20.8f"),10.123334,10.3453)
print( format(102324.12,">20.8f"),10.12323334,10.231)
print( format(1032435.123,">20.8f"),10.1243334,10.3532)
print( format(10634324.1234,">20.8f"),10.1236334,12054)
print( format(102345343.12345,">20.8f"),10.1233734,10.234)
print( format(1023423534.123456,">20.8f"),10.12334,10.987)


